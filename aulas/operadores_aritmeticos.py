adicao = 10 + 10
print('Adição:', adicao)

subtracao = 10 - 5
print('Subtracao:', subtracao)

multiplicacao = 10 * 10
print('Multiplicacao: ', multiplicacao)

divisao = 10 / 2.2  #float, sempre vai retornar float e sempre vai mostrar as casas
print('Divisão:', divisao)

divisao_inteira = 10 // 2.2  #se vc botar um float ele vai trazer um float, mas sem as suas casas decimais
# se vc botar um int dividindo outro int vai dar um int como resposta e se botar um float em qualquer lugar, vai retornar um float
print('Divisão Inteira:', divisao_inteira)

exponenciacao = 2 ** 10  #isso é potenciação, o primeiro número vai ser elevado ao segundo, exemplo(num1 ** num2), sempre deve haver dois asteriscos para potenciação
#tome cuidado, pois dependendo do resultado pode dar um número muito grande e vc pode estourar a memória do seu pc
print('Exponenciação:', exponenciacao)

