#tipos int e float

#int -> número inteiro
#o tipo int representa qualquer número positivo ou negativo
#int sem sinal é considerado positivo
print(1) #int positivo
print(-1) #int negativo
print(0) #int neutro

#float -> número com ponto flutuante
#o tipo float representa qualquer número positivo ou negativo com ponto flutuante
#float sem sinal é considerado positivo
print(1.1) #float positivo
#na programação sempre usamos ponto para números decimais, nunca vírgula
#vírgula em python significa separação de caracteres, mas na programação vírgula é usada para outra coisa
print(-1.1) #float negativo
print(0.0) #float neutro

#a função type mostra o tipo q o python infertiu ao valor
print(type(1.1))